class TestExtractFrames:
    def test_extraction_count(self, tmp_path, monkeypatch):
        """extract_frames at 1 fps should produce ~10 frames for a 10s video."""
        from backend.pipeline.video import extract_frames, save_video_file
        import backend.pipeline.video as video_module

        monkeypatch.setattr(video_module, "SESSIONS_DIR", tmp_path)

        meta = save_video_file(str(TEST_VIDEO_PATH), session_id="extract-test")
        frames = extract_frames(meta, fps=1.0, output_dir=str(tmp_path / "frames"))

        # 10 second video @ 1 fps -> ~10 frames (allow ±1 for rounding)
        assert 9 <= len(frames) <= 11

    def test_frames_are_sorted_by_timestamp(self, tmp_path, monkeypatch):
        from backend.pipeline.video import extract_frames, save_video_file
        import backend.pipeline.video as video_module

        monkeypatch.setattr(video_module, "SESSIONS_DIR", tmp_path)

        meta = save_video_file(str(TEST_VIDEO_PATH), session_id="sort-test")
        frames = extract_frames(meta, fps=1.0, output_dir=str(tmp_path / "frames"))

        timestamps = [f.timestamp for f in frames]
        assert timestamps == sorted(timestamps)

    def test_frame_files_exist(self, tmp_path, monkeypatch):
        from backend.pipeline.video import extract_frames, save_video_file
        import backend.pipeline.video as video_module

        monkeypatch.setattr(video_module, "SESSIONS_DIR", tmp_path)

        meta = save_video_file(str(TEST_VIDEO_PATH), session_id="files-test")
        frames_dir = tmp_path / "frames"
        frames = extract_frames(meta, fps=1.0, output_dir=str(frames_dir))

        for f in frames:
            assert Path(f.file_path).exists(), f"Frame file missing: {f.file_path}"

    def test_higher_fps_yields_more_frames(self, tmp_path, monkeypatch):
        from backend.pipeline.video import extract_frames, save_video_file
        import backend.pipeline.video as video_module

        monkeypatch.setattr(video_module, "SESSIONS_DIR", tmp_path)

        meta = save_video_file(str(TEST_VIDEO_PATH), session_id="fps-test")
        frames_1fps = extract_frames(meta, fps=1.0, output_dir=str(tmp_path / "frames_1"))
        frames_2fps = extract_frames(meta, fps=2.0, output_dir=str(tmp_path / "frames_2"))

        assert len(frames_2fps) > len(frames_1fps)


