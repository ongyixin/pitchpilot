class TestExtractAudio:
    def test_audio_extracted_from_real_video(self, tmp_path, monkeypatch):
        """extract_audio raises RuntimeError when the video has no audio stream."""
        if shutil.which("ffmpeg") is None:
            pytest.skip("ffmpeg not installed")

        from backend.pipeline.video import extract_audio, save_video_file
        import backend.pipeline.video as video_module

        monkeypatch.setattr(video_module, "SESSIONS_DIR", tmp_path)

        # The synthetic test video is video-only (no audio stream) —
        # extract_audio should raise RuntimeError with a clear message
        meta = save_video_file(str(TEST_VIDEO_PATH), session_id="audio-test")
        with pytest.raises(RuntimeError):
            extract_audio(meta, output_dir=str(tmp_path / "audio"))

    def test_audio_raises_without_ffmpeg(self, tmp_path, monkeypatch):
        """extract_audio should raise RuntimeError when ffmpeg is missing."""
        from backend.pipeline import video as video_module
        from backend.pipeline.video import extract_audio, save_video_file

        monkeypatch.setattr(video_module, "SESSIONS_DIR", tmp_path)
        monkeypatch.setattr("shutil.which", lambda _: None)

        meta = save_video_file(str(TEST_VIDEO_PATH), session_id="no-ffmpeg-test")

        with pytest.raises(RuntimeError, match="ffmpeg"):
            extract_audio(meta, output_dir=str(tmp_path / "audio"))
